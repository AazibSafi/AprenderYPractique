package com.algorithms.aprenderypractique.Toptal.attemp1;

public class Node {
    Character key;
    int value;

    public Node(Character key, int value) {
        this.key = key;
        this.value = value;
    }

    public Character getKey() {
        return key;
    }

    public void setKey(Character key) {
        this.key = key;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

}
